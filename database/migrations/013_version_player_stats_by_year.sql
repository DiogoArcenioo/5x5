-- Versiona overall e skills por temporada.
-- O cadastro de players passa a conter somente a identidade do jogador.
-- Cada player_team_years guarda a fotografia estatistica daquele ano.

ALTER TABLE player_team_years
  ADD COLUMN overall smallint,
  ADD COLUMN firepower smallint,
  ADD COLUMN entrying smallint,
  ADD COLUMN trading smallint,
  ADD COLUMN opening smallint,
  ADD COLUMN clutching smallint,
  ADD COLUMN sniping smallint,
  ADD COLUMN utility smallint;

-- Preserva todos os dados atuais antes de aplicar as fotografias anuais
-- historicas que ja estavam documentadas nas migrations de carga.
UPDATE player_team_years pty
SET overall = players.overall,
    firepower = players.firepower,
    entrying = players.entrying,
    trading = players.trading,
    opening = players.opening,
    clutching = players.clutching,
    sniping = players.sniping,
    utility = players.utility
FROM players
WHERE players.id = pty.player_id;

